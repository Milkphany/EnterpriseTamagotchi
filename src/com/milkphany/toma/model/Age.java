public class Age {

    private Double currentAge;
    private Double happiestAge;
    private Double weirdestAge;

    public Double getCurrentAge() {
        return currentAge;
    }

    public void setCurrentAge(Double currentAge) {
        this.currentAge = currentAge;
    }

    public Double getHappiestAge() {
        return happiestAge;
    }

    public void setHappiestAge(Double happiestAge) {
        this.happiestAge = happiestAge;
    }

    public Double getWeirdestAge() {
        return weirdestAge;
    }

    public void setWeirdestAge(Double weirdestAge) {
        this.weirdestAge = weirdestAge;
    }
}
